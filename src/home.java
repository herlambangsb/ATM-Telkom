    private void btsetorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsetorActionPerformed
        // TODO add your handling code here:
        int hasil, saldo;
        int s = Integer.parseInt(txsetor.getText());
        
        if(s%100!=0){
            JOptionPane.showMessageDialog(null, "Mesin ATM BRI tidak menerima uang koin \nSilahkan ulangi kembali!", "Transaksi Gagal", 0);
        }
        else if(s<50000){
            JOptionPane.showMessageDialog(null, "Besaran minimal setor jumlah tunai adalah Rp.50000,00", "ERROR", 0);
        }
        else if(s>=50000){
            tab += s;
            JOptionPane.showMessageDialog(null, "Saldo Anda saat ini sebesar : " + tab);
            infsaldo.setText("Rp"+ tab);
        }
        
        txsetor.setVisible(false);
        btsetor.setVisible(false);
        
    }//GEN-LAST:event_btsetorActionPerformed

