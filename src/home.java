    private void bttarikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttarikActionPerformed
        // TODO add your handling code here:
        int t = Integer.parseInt(txtarik.getText());
        
        
            if(t>tab)
                JOptionPane.showMessageDialog(null, "Saldo Anda tidak mencukupi \nSilahkan lakukan penyetoran", "Transaksi Gagal", 0);
            else if(t<50000)
            JOptionPane.showMessageDialog(null, "Besaran minimal pengabilan tunai adalah Rp.50000,00", "ERROR", 0);
            
            else{
                tab = tab -t;
                JOptionPane.showMessageDialog(null, "Saldo Anda saat ini sebesar : " + tab);
                infsaldo.setText("Rp"+tab);
                if(tab<=50000)
                    JOptionPane.showMessageDialog(null, "Saldo minimal harus Rp 50000,00, Segera lakukan penyetoran untuk menghindari penutupan akun", "CAUTION",2);
        }
            
        bttarik.setVisible(false);
        txtarik.setVisible(false);
        
    }//GEN-LAST:event_bttarikActionPerformed

